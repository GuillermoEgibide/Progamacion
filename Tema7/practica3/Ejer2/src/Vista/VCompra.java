    private void jfPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jfPrecioActionPerformed
        try{
            if (jfPrecio.getText().isEmpty())
                throw new DatoNoValido();
            precio = Float.parseFloat(jfPrecio.getText());
            jfPrecio.setEnabled(false);
       
            if (!Ejer2.Proveedores(cbProveedor))
                throw new DatoNoValido();
      
            cbProveedor.setEnabled(true);
        }
        catch(DatoNoValido e){
           JOptionPane.showMessageDialog(null, "Corriga el error");
       }
       catch(Exception e){
           JOptionPane.showMessageDialog(null, "Error");
       }
    }//GEN-LAST:event_jfPrecioActionPerformed

